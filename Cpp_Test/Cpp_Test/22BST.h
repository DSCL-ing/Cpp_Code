//BST binary search tree /binary sort tree
/**
 * 中文名:二叉搜索树或二叉排序树
 * 注:空树也是BST
 * 性质:
 * 左子树的所有键值小于根,根小于右子树的所有键值
 * 
 * 特点
 * 1.BST的中序/中根遍历的结果是升序排序
 * 
 * 2.默认的BST不允许冗余 -- 即不允许有重复的值
 * 
 * 3.插入的第一个值就是根
 * 
 * 4.删除
 * a.叶子结点:直接删
 * b.单路:续接
 * c.多路:尾删除法:用左子树的最大结点或右子树的最小结点
 * 
 * 5.树的最大最小结点
 * a.一棵树中最大结点是最右结点(是右结点,且是最右边)
 * b.一棵树中最小结点是最左结点(是左结点,且是最左边)
 */





